class Hello:
    def greet(self):
        return "Hello, World!"
    
    def farewell(self):
        return "Goodbye, World!"
    
if __name__ == "__main__":
    hello = Hello()
    print(hello.greet())
    print(hello.farewell())

# This is a simple demonstration of a class with greeting methods.
# You can run this script to see the output of the greet and farewell methods.